// Switch statements

let ch = "B";

switch (ch) {
  case "a":
    console.log(`${ch} is a vowel`);
    break;

  case "b":
    console.log(`${ch} is a consonant`);
    break;

  case "A":
    console.log(`${ch} is a vowel in upper case`);
    break;

  case "B":
    console.log(`${ch} is a consonant in upper case`);
    break;

  default:
    console.log(`${ch} is an integer`);
}
